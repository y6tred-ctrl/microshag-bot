def encouragement_text(total_done: int) -> str:
    if total_done == 0:
        return "Ты в самом начале пути. Это нормально ❤️"
    elif total_done <= 5:
        return "Отличный старт! Первые шаги — самые важные 🌱"
    elif total_done <= 20:
        return "Уже {total_done} шагов! Это хороший темп 💪"
    elif total_done <= 50:
        return "{total_done} шагов — ты серьёзно двигаешься вперёд 🚀"
    else:
        return "Больше {total_done} шагов! Ты — машина маленьких побед 🏆"


def format_steps_list(steps: list[dict]) -> str:
    return "\n".join(f"{i+1}. {s['text']}" for i, s in enumerate(steps))


def format_steps_for_review(steps: list[str]) -> str:
    return "\n".join(f"{i+1}. {s}" for i, s in enumerate(steps))
